# 聚合函数 —— 根据市场返回对应的种子教训
# ============================================================================

def get_seed_lessons(market: str) -> List[Tuple[str, str]]:
    """根据市场类型返回对应的种子教训合集（公共 + 市场特有）。

    Args:
        market: 市场标识，取值为 "a_share"、"hk"、"us"、"other"。
                "other" 只返回公共教训。

    Returns:
        List[Tuple[str, str]]: (situation, recommendation) 列表。
    """
    lessons = list(COMMON_LESSONS)  # 公共教训始终包含

    if market == "a_share":
        lessons.extend(ASHARE_LESSONS)
    elif market == "hk":
        lessons.extend(HK_LESSONS)
    elif market == "us":
        lessons.extend(US_LESSONS)
    # "other" 仅包含公共教训

    return lessons


def get_lesson_count(market: str) -> int:
    """获取指定市场的总教训数（便于调试和监控）。

    Args:
        market: 市场标识。

    Returns:
        int: 总教训数。
    """
    return len(get_seed_lessons(market))
